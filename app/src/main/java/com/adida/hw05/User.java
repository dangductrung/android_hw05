package com.adida.hw05;

public class User {
    public String userId;
    public String name;
    public int imageId;
    public String className;
    public double grade;

    public User(String userId, String name, int imageId, String className, double grade) {
        this.userId = userId;
        this.name = name;
        this.imageId = imageId;
        this.className = className;
        this.grade = grade;
    }
}
